package Operators;

public class UnaryOp {
    public static void main(String[] args) {
        /*
         * Unary operators 
         * +,-,++,--;
         */
         
         int x = 4;
         int a = +2;
         int b = -3;
         int c = ++x;  
         int d = x++;  
         int e = --x;  
         int f = x--;  
         
         System.out.println(a);
         System.out.println(b);
         System.out.println(c);
    }
}

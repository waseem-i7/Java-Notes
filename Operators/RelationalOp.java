package Operators;

import javax.swing.text.html.HTMLDocument.RunElement;

public class RelationalOp {
    public static void main(String[] args) {
        
        /*
         * Relational Operators
         * >,<,<=,>=,==,!=
         */


         boolean result = 3>4;
         System.out.println(result);

         result = 4<13;
         System.out.println(result);

         result = 5<=5;
         System.out.println(result);

         result = 6>=11;
         System.out.println(result);

         result = 6 ==5;
         System.out.println(result);

         result = 6 != 5;
         System.out.println(result);
    }
}
